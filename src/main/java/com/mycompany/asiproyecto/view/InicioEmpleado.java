    private void cargarMisOfertas() {
        OfertaDAO ofertaDAO = new OfertaDAO();
        // Guardamos todas las ofertas en la lista local
        todasLasOfertas = ofertaDAO.obtenerOfertasPorEmpleado(empleadoEmpresa.getIdEmpleado());
        // Mostramos todas inicialmente
        actualizarPanelOfertas(todasLasOfertas);
    }

    private void actualizarPanelOfertas(List<Oferta> ofertasParaMostrar) {
        OfertaDAO ofertaDAO = new OfertaDAO();

        panelMisOfertas.removeAll();
        panelMisOfertas.setLayout(new java.awt.BorderLayout());

        JPanel container = new JPanel(new java.awt.GridLayout(0, 2, 10, 10)); // 2 columns
        container.setBackground(new java.awt.Color(240, 240, 240));

        for (Oferta o : ofertasParaMostrar) {
            OfertaPanel op = new OfertaPanel(o, true);
            op.addEliminarListener(e -> {
                int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar esta oferta?",
                        "Confirmar eliminación", javax.swing.JOptionPane.YES_NO_OPTION);
                if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                    if (ofertaDAO.eliminarOferta(o.getIdOferta())) {
                        cargarMisOfertas(); // Refresh
                        javax.swing.JOptionPane.showMessageDialog(this, "Oferta eliminada correctamente");
                    }
                }
            });
            op.addEditarListener(e -> {
                javax.swing.JOptionPane.showMessageDialog(this, "Funcionalidad de editar próximamente.");
            });
            container.add(op);
        }

        JScrollPane scroll = new JScrollPane(container);
        // Evita que el ScrollPane fuerce un cambio de tamaño en el panel padre
        scroll.setPreferredSize(new java.awt.Dimension(0, 0));
        panelMisOfertas.add(scroll, java.awt.BorderLayout.CENTER);
        jLabel24.setText("OFERTAS ENCONTRADAS: " + ofertasParaMostrar.size());
        panelMisOfertas.revalidate();
        panelMisOfertas.repaint();
    }

    private void agregarDatePickersPublicarOFerta() {
        DatePickerSettings dateSettings1 = new DatePickerSettings();
        dateSettings1.setFormatForDatesCommonEra("dd-MM-yyyy");
        DatePickerSettings dateSettings2 = new DatePickerSettings();
        dateSettings2.setFormatForDatesCommonEra("dd-MM-yyyy");
        datePickerInicio = new DatePicker(dateSettings1);
        datePickerFin = new DatePicker(dateSettings2);
        datePickerInicio.setDate(LocalDate.now());
        datePickerFin.setDate(LocalDate.now());
        javax.swing.GroupLayout layout = (javax.swing.GroupLayout) informacionOfertaPanel.getLayout();
        layout.replace(datePickerPlaceholder1, datePickerInicio);
        layout.replace(datePickerPlaceholder2, datePickerFin);
        informacionOfertaPanel.revalidate();
        informacionOfertaPanel.repaint();
    }

